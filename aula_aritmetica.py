#declarando Variaveis do tipo int
a = 5
b = 2

#Realizando operações de Adição
adicao = a + b
print('Adição: ' + str(adicao))

#realizando operações de Subtração
subtracao = a - b
print('Subtração: ' + str(subtracao))

#realizando operações de Multiplicação
multiplicacao = a * b
print('Multiplicação: ' + str(multiplicacao))

#realizando operações de Divisão
divisao = a / b
print('Divisão: ' + str(divisao))

#realizando operações de Módulo (Resto da Divisão)
modulo = a % b
print('Modulo: ' + str(modulo))

#realizando operações de Potenciação
potencia = a ** b
print('Potenciação: ' + str(potencia))

#Realizando operações de Divisão por inteiro
divisaoInteiro = a // b
print('Divisão Inteiro: ' + str(divisaoInteiro))
